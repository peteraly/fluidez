// Helper functions
export function getAllWords() {
  let allWords = [];
  Object.values(VOCABULARY).forEach(category => {
    allWords = allWords.concat(category.words);
  });
  return allWords;
}

export function getWordsByCategory(categoryKey) {
  return VOCABULARY[categoryKey]?.words || [];
}

export function getWordsByFrequency(frequency) {
  return getAllWords().filter(word => word.frequency === frequency);
}

export function getTotalWordCount() {
  return getAllWords().length;
}

export function getCategories() {
  return Object.entries(VOCABULARY).map(([key, value]) => ({
    key,
    name: value.name,
    emoji: value.emoji,
    count: value.words.length
  }));
}

export function searchWords(query) {
  const lowerQuery = query.toLowerCase();
  return getAllWords().filter(word => 
    word.spanish.toLowerCase().includes(lowerQuery) ||
    word.english.toLowerCase().includes(lowerQuery)
  );
}

